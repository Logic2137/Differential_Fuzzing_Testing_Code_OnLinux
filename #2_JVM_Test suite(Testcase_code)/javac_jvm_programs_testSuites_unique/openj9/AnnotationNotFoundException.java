package org.openj9.test.annotation;


public class AnnotationNotFoundException extends Exception {
	private static final long serialVersionUID = 3258129146209972275L;

	public AnnotationNotFoundException() {
		super();
	}

	public AnnotationNotFoundException(String arg0) {
		super(arg0);
	}

	public AnnotationNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AnnotationNotFoundException(Throwable arg0) {
		super(arg0);
	}

}
